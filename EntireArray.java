public class EntireArray {
        public static void main(String[] args)
        {
            int[] arr = { -100, -75, 55, 100, 40, 30, 20, 25, 12 };

            System.out.print("Elements of given array are: ");
            for (int i = 0; i < arr.length; i++)
            {
                System.out.print(arr[i] + " ");
            }
        }
    }




